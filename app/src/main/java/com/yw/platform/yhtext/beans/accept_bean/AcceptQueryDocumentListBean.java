package com.yw.platform.yhtext.beans.accept_bean;


import com.yw.platform.yhtext.beans.commonbeans.DocumentBean;
import com.yw.platform.yhtext.beans.commonbeans.PagingInfoBean;

import java.io.Serializable;
import java.util.ArrayList;
import java.util.List;

public class AcceptQueryDocumentListBean  implements Serializable{
    String message;
    boolean success;
    String notification;
    int code;
    List<DocumentBean> documents=new ArrayList<>();

     PagingInfoBean pagingInfo = new PagingInfoBean();

    public PagingInfoBean getPagingInfo() {
        return pagingInfo;
    }

    public void setPagingInfo(PagingInfoBean pagingInfo) {
        this.pagingInfo = pagingInfo;
    }

    public String getMessage() {
        return message;
    }

    public void setMessage(String message) {
        this.message = message;
    }

    public boolean isSuccess() {
        return success;
    }

    public void setSuccess(boolean success) {
        this.success = success;
    }

    public String getNotification() {
        return notification;
    }

    public void setNotification(String notification) {
        this.notification = notification;
    }

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public List<DocumentBean> getDocuments() {
        return documents;
    }

    public void setDocuments(List<DocumentBean> documents) {
        this.documents = documents;
    }
}

