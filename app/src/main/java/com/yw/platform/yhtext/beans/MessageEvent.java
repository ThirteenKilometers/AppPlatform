public class MessageEvent<T> {

    int code;
    String msg;
    T data;
    T dataContent;
    ResponseModel responseData;
    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }

    public Object getData() {
        return data;
    }

    public void setData(T data) {
        this.data = data;
    }

    public T getDataContent() {
        return dataContent;
    }

    public void setDataContent(T dataContent) {
        this.dataContent = dataContent;
    }

    public ResponseModel getResponseData() {
        return responseData;
    }

    public void setResponseData(ResponseModel responseData) {
        this.responseData = responseData;
    }
}
